function solution(arr1, arr2) {
  let answer = [];
  let n = arr1.length;
  let m = arr2.length;
  // ν¬μΈν„°μ κ°’μ„ 0μΌλ΅ μ΄κΈ°ν™”μ‹μΌμ¤€λ‹¤.
  let p1 = (p2 = 0);

  // ν¬μΈν„° λ‘ μ¤‘μ— ν•λ‚κ°€ λ°°μ—΄μ κΈΈμ΄μ™€ κ°’μ΄ κ°™μ•„μ§€λ” μκ°„ while λ¬Έμ΄ μΆ…λ£λλ‹¤.
  while (p1 < n && p2 < m) {
    if (arr1[p1] < arr2[p2]) answer.push(arr1[p1++]);
    else answer.push(arr2[p2++]);
  }

  // λ‘ μ¤‘ ν•λ‚μ λ°°μ—΄μ„ κ°€λ¦¬ν‚¤λ” ν¬μΈν„°κ°€ λ°°μ—΄μ κΈΈμ΄λ¥Ό λ„μ–΄μ„°μΌλ―€λ΅, μ•„λ‹ λ°°μ—΄μ λ‚¨μ€ λ‚λ¨Έμ§€ μ›μ†λ§ λ„£μ–΄μ¤€λ‹¤.
  while (p1 < n) {
    answer.push(arr1[p1++]);
  }

  while (p2 < m) {
    answer.push(arr2[p2++]);
  }

  return answer;
}
